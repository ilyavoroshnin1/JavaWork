package Homework2;

public class Homework3 {
    public static void main(String[] args) {

        //Кол-во четных чисел в диапазоне от 30 до 61
        int numberOfEvenNumbers = 0;

        for (int i = 30; i <= 60; i++) {
            if (i % 2 == 0) ;
            {
                numberOfEvenNumbers ++;
            }

        }
        System.out.println("Кол-во четных цифр" + " " + numberOfEvenNumbers);
    }
}
